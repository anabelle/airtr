export const aircraftByFamilyId = new Map<string, AircraftModel[]>();
for (const aircraft of aircraftModels) {
  const family = aircraftByFamilyId.get(aircraft.familyId) || [];
  family.push(aircraft);
  aircraftByFamilyId.set(aircraft.familyId, family);
}

export const aircraftByTier = new Map<number, AircraftModel[]>();
for (const aircraft of aircraftModels) {
  const tier = aircraftByTier.get(aircraft.unlockTier) || [];
  tier.push(aircraft);
  aircraftByTier.set(aircraft.unlockTier, tier);
}

export function getAircraftById(id: string): AircraftModel | undefined {
  return aircraftModels.find((a) => a.id === id);
}

export function getAircraftByType(type: AircraftModel["type"]): AircraftModel[] {
  return aircraftModels.filter((a) => a.type === type);
}
